export const squares = [];

export const createGrid = () => {
  for (const i of layout) {
    const div = document.createElement('div');
    div.classList.add(addClassCss(i));
    grid.insertAdjacentElement('afterbegin', div);
    squares.push(div);
  }
};

const addClassCss = (num) => {
  switch (num) {
    case 0:
      return 'pac-dot';
    case 1:
      return 'wall';
    case 2:
      return 'ghost-lair';
    case 3:
      return 'power-pellet';
    case 4:
      return 'pac-dot';
    case 7:
      return 'pacman';
    default:
      break;
  }
};

export const pacmanAddMouth = (div) => {
  let mouth = document.createElement('div');
  mouth.classList.add('pacman__mouth');
  div.appendChild(mouth);
};